//WAP for calculating and display area of a circle. The area is not formatted and displayed as it is.

class Prgm80 {

	public static void main(String args[]){
		
		final double PI = (double)22/7;	//constant
		double r= 15.5;	//radius
		double area= PI*r*r;
		System.out.println("Area= "+area);
	}
}
